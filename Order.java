public class Order 
{
    /* 
 * this is the Order class in the restaurant program
 * 
 * - this class is used to represent the order 
 * that a customer makes in the restaurant.
 *
 */
	
	private String name;
    
    public Order(String name) 
    {
        this.name = name;
    }
    
    public String getName() 
    {
        return name;
    }


}

